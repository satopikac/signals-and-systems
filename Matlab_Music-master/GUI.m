    case 'radiobutton2'
        disp('自定义函数');
        yuepu = str2num(handles.edit1.String);
        jiepai = str2num(handles.edit2.String);
        fs = 8000;
        one_step = str2num(handles.edit3.String);
        basic_f = str2num(handles.edit4.String);
        method = 5-handles.popupmenu2.Value+1;
        song = sound_song(yuepu,jiepai,one_step,fs,basic_f,method);
        
    case 'radiobutton3'
        disp('Canon');
        Canon;
    case 'radiobutton4'
        disp('天空之城');
        sky_town;
    case 'radiobutton5'
        disp('东方红');
        dfh;
end

